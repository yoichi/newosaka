def translate(data):
    """translate input string to Osaka dialect.

    :param str data: input string
    :rtype: str
    :return: translated string
    """
    translated = ""
    while data:
        for rule in _HENKAN:
            if data.startswith(rule[0]):
                translated += rule[1]
                data = data[len(rule[0]):]
                break
        else:
            translated += data[0]
            data = data[1:]
    return translated
